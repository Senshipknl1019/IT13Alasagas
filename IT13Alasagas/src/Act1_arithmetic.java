public class Act1_arithmetic {
    public static void main(String[] args) {
        
        int firstnumber = 125;
        int secondnumber = 24;

        int addition = firstnumber + secondnumber;
        int subtraction = firstnumber - secondnumber;
        int multiply = firstnumber * secondnumber;
        int divide = firstnumber / secondnumber;
        int module = firstnumber % secondnumber;

        System.out.println(addition);    // Result of addition
        System.out.println(subtraction); // Result of subtraction
        System.out.println(multiply);    // Result of multiplication
        System.out.println(divide);      // Result of division
        System.out.println(module);      // Result of modulo
    }
}
